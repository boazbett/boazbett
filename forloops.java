class forloops{
    public static void main (String[] args){
        //for loop takes three statements inside it(1.start, 2.end, 3.incrementing)
        System.out.println("This loop will output 6 - 21 in increments of 3");
        for(int counter = 6; counter <=21; counter+=3){
            System.out.println(counter);
        }
    }
}