class mathclassmethods{
    public static void main(String[] args){
        System.out.println(Math.abs(-26.7)); //will give absolute value
        System.out.println(Math.ceil(7.4));//will round UP your #
        System.out.println(Math.floor(7.8));//will round DOWN your #
        System.out.println(Math.sqrt(26.7));//squareroot
        System.out.println(Math.pow(26.7,.5));//first # power second #
        System.out.println(Math.sin(26.7));//sine...also cos, tan, acos[^-1], asin, atan
        System.out.println(Math.round(26.7));//round your # closest integer to the value
        System.out.println(Math.log10(26.7));//log to the base 10
        System.out.println(Math.max(26.7,47.43));//maximum of two values...also "min"
        System.out.println(Math.exp(3.142));//exponential 
    }
}